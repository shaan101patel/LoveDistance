/**
 * Reads apps/mobile/src/assets/couples_prompt_library.csv and writes
 * apps/mobile/src/data/generatedCouplesPromptLibrary.ts
 */
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname as pathDirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = pathDirname(fileURLToPath(import.meta.url));

const CATEGORY_SLUG = {
  'Appreciation and gratitude': 'appreciation_gratitude',
  'Daily check-in and mood': 'daily_check_in_mood',
  'Emotional intimacy': 'emotional_intimacy',
  'Romantic intimacy': 'romantic_intimacy',
  'Physical intimacy': 'physical_intimacy',
  'Sexual intimacy': 'sexual_intimacy',
  'Conflict and repair': 'conflict_repair',
  'Communication and understanding': 'communication_understanding',
  'Future goals and planning': 'future_goals_planning',
  'Shared memories and nostalgia': 'shared_memories_nostalgia',
  'Fun and playful': 'fun_playful',
  'Stress and support': 'stress_support',
  'Habits and rituals': 'habits_rituals',
  'Distance and reunion': 'distance_reunion',
  'Commitment and long-term vision': 'commitment_long_term_vision',
};

/** Single CSV line; fields may be quoted. */
function parseCsvLine(line) {
  const out = [];
  let cur = '';
  let i = 0;
  let inQ = false;
  while (i < line.length) {
    const ch = line[i];
    if (inQ) {
      if (ch === '"') {
        if (line[i + 1] === '"') {
          cur += '"';
          i += 2;
          continue;
        }
        inQ = false;
        i++;
        continue;
      }
      cur += ch;
      i++;
      continue;
    }
    if (ch === '"') {
      inQ = true;
      i++;
      continue;
    }
    if (ch === ',') {
      out.push(cur);
      cur = '';
      i++;
      continue;
    }
    cur += ch;
    i++;
  }
  out.push(cur);
  return out;
}

function main() {
  const csvPath = join(__dirname, '../src/assets/couples_prompt_library.csv');
  const outPath = join(__dirname, '../src/data/generatedCouplesPromptLibrary.ts');
  const raw = readFileSync(csvPath, 'utf8');
  const lines = raw.split(/\r?\n/).filter((l) => l.trim().length > 0);
  const header = parseCsvLine(lines[0]);
  if (header.join(',') !== 'category,prompt_num,prompt,morning_wake_up,nsfw') {
    throw new Error(`Unexpected CSV header: ${header.join('|')}`);
  }

  const entries = [];
  for (let li = 1; li < lines.length; li++) {
    const cols = parseCsvLine(lines[li]);
    if (cols.length !== 5) {
      throw new Error(`Line ${li + 1}: expected 5 columns, got ${cols.length}: ${lines[li].slice(0, 80)}`);
    }
    const [categoryLabel, promptNumStr, prompt, morningWakeUp, nsfwStr] = cols;
    const categoryId = CATEGORY_SLUG[categoryLabel];
    if (!categoryId) {
      throw new Error(`Unknown category label: ${JSON.stringify(categoryLabel)}`);
    }
    const promptNum = Number(promptNumStr);
    if (!Number.isFinite(promptNum)) {
      throw new Error(`Bad prompt_num on line ${li + 1}`);
    }
    const nsfw = nsfwStr.trim().toLowerCase() === 'yes';
    if (!['Morning', 'Anytime', 'Not morning'].includes(morningWakeUp)) {
      throw new Error(`Bad morning_wake_up on line ${li + 1}: ${morningWakeUp}`);
    }
    entries.push({ categoryId, categoryLabel, promptNum, text: prompt, morningWakeUp, nsfw });
  }

  const headerTs = `/**
 * AUTO-GENERATED by scripts/generate-prompt-library.mjs — do not edit by hand.
 * Source: src/assets/couples_prompt_library.csv
 */
`;

  const typeBlock = `export type CouplesPromptMorningSlot = 'Morning' | 'Anytime' | 'Not morning';

export type CouplesPromptLibraryEntry = {
  categoryId: string;
  categoryLabel: string;
  promptNum: number;
  text: string;
  morningWakeUp: CouplesPromptMorningSlot;
  nsfw: boolean;
};

`;

  const jsonRows = entries.map((e) =>
    JSON.stringify({
      categoryId: e.categoryId,
      categoryLabel: e.categoryLabel,
      promptNum: e.promptNum,
      text: e.text,
      morningWakeUp: e.morningWakeUp,
      nsfw: e.nsfw,
    }),
  );

  const body = `export const COUPLES_PROMPT_LIBRARY: readonly CouplesPromptLibraryEntry[] = [\n${jsonRows
    .map((j) => `  ${j} as CouplesPromptLibraryEntry,`)
    .join('\n')}\n];\n`;

  mkdirSync(pathDirname(outPath), { recursive: true });
  writeFileSync(outPath, `${headerTs}${typeBlock}${body}`, 'utf8');
  console.log(`Wrote ${entries.length} entries to ${outPath}`);
}

main();
